// Get municipality name from DANE code
export function getMunicipioName(code: string): string {
  if (!code) return "";
  
  // Try exact match first
  if (municipiosDane[code]) {
    return municipiosDane[code];
  }
  
  // Try with padding to 8 digits
  const paddedCode = code.padStart(8, "0");
  if (municipiosDane[paddedCode]) {
    return municipiosDane[paddedCode];
  }
  
  // Try appending "000" for 5-digit codes
  if (code.length === 5) {
    const extendedCode = code + "000";
    if (municipiosDane[extendedCode]) {
      return municipiosDane[extendedCode];
    }
  }
  
  // Return the code if no match found
  return code;
}

// Format value with comma for thousands (Colombian format)
export function formatValorQR(value: number): string {
  if (!value || isNaN(value)) return "0";
  return value.toLocaleString("es-CO").replace(/\./g, ",");
}
